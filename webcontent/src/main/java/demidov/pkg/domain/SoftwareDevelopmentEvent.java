package demidov.pkg.domain;

public class SoftwareDevelopmentEvent extends UserEvents {

	
	
	private String descriptionDevelopment;	

		public String getDescriptionDevelopment() {
			return descriptionDevelopment;
		}
	
		public void setDescriptionDevelopment(String descriptionDevelopment) {
			this.descriptionDevelopment = descriptionDevelopment;
		}

		
		
	private String additionalInfoDevelopment;

		public String getAdditionalInfoDevelopment() {
			return additionalInfoDevelopment;
		}
	
		public void setAdditionalInfoDevelopment(String additionalInfoDevelopment) {
			this.additionalInfoDevelopment = additionalInfoDevelopment;
		}
		
	
	
}